     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $settings = Setting::first();
        if ($settings == null) {
            $setting = new Setting();
        } else {
            $setting = $settings;
        }
        $setting->karmozd = $request->percent;
        if ($request->max_price != null) {
            $setting->max_price = $request->max_price;
        }
        $setting->save();
        return redirect()->back()->with("success", "your setting made successfully");
    }

